  <h3>Rules to trade by:</h3><br>
  <p>
  <b>Rule 1</b>: Do not invest all of your money in only 1 or 2 assets. Invest up to 15% of your total capital to one altcoin position.<br>
  <b>Rule 2</b>: Buy at dip (depth of the order book) and sell at the top of order book.<br>
  <b>Rule 3</b>: Do not panic sell (unless it is a legit dump of an asset because expectations were not met or negative news seems to be valid)<br>
  <b>Rule 4</b>: Do not be greedy. More often secure 5-10% returns rather than wait for 20-30%. Compounding small victories will ensure larger
  capital in the long-run. Legit (not Pump and Dump schemes) >20% runs happen occasionally.<br>
  <b>Rule 5</b>: Buy at rumors and sell at or even before the news. More often than not the asset is dumped, especially at roadmap or update releases.
  On the other hand, major partnerships (not conferences) result in huge hikes in prices.<br>
  <b>Rule 6</b>: Always take a profit. Do not fall in love with the asset. Pumps or hikes in prices do not last for ever and everything can turn to
  the worst within seconds. Unless specified otherwise, take a profit at 5-10% and re-enter at dip.<br>
  <b>Rule 7</b>: You will receive a signal with a Buy and Sell range. Observe the given asset, and buy at the lower-end of the range and sell at
  the or even lower end of the first target, unless specified otherwise.<br>
  <b>Rule 8</b>: Be patient. Sometimes positions do not perform as expected. This will happen. However, you do not incur a loss if you do not sell.
  But do not let a position to drop to extreme levels, such as 20%, because a run from that point, may not even bring you to break-even.<br>
  <b>Rule 9</b>: In connection to Rule 8, use Stop-loss at your own risk-tolerance level. We suggest 5 to 10% due to the volatility of the market.<br>
  <b>Rule 10</b>: Ensure that you hold a diversified portfolio at any point of time. The market may shift at any point of time and it usually happens
  at the worst possible time, e.g. while you are sleeping or not able to rebalance your portfolio due to unavailability. Golden rule of thumb
  would be not to invest more than 15% of your capital in one asset, unless it is Bitcoin.<br>
  <b>Rule 11</b>: Do not fomo (fear of missing out). Sometimes it is a trap. Instead, at the beginning, observe how the market behaves and dynamics
  change rapidly. Engage only when you will be comfortable to FOMO and able to time entry and exit correctly.
  </p>


