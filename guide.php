  <h3>What are Cryptocurrencies?</h3><br>
  <p>
  How much of the money supply in the world is digital? Most estimates place it around 90%. That is, 90% of all money in existence doesn’t really exist except for ones and zeros on a bank’s server. Countries used to print money, sometimes with devastating effects like hyperinflation, but now it’s far far easier. Just add a couple of zeros to the end of an entry in your database and voila, you’ve created another trillion dollars.<br> If you’ve been paying attention to the news lately you will have seen countless reports on hacks: from the presidential elections to SONY and HBO. How much longer do you think it will be before your money, the ones and zeros on the bank server, become vulnerable? These were among the first reasons for the creation of Bitcoin. Immune to inflation, immune to hacking, but it doesn’t stop there. Over the past decade the community of developers have started to realize that the scope of what they’re creating is much larger than first meets the eye. If you want to,<br> you can jump straight to Use Cases to see what they are currently being used for and what the future holds.<br>
  If you’re looking for a precise definition then cryptocurrencies can be defined as: A digital currency in which encryption techniques are used to regulate the generation of units of currency and verify the transfer of funds, operating independently of a central bank.<br>
  The term cryptocurrencies can be misleading as some of the projects I’ll be discussing in this guide aren’t currencies. Some are assets, some are “platforms”, some are businesses. For the sake of accuracy, I will simply refer to them as cryptos<br>
  Despite all this talk you will still likely be asking why should you care about any of this. Below I will outline where the true value of cryptos lie.<br>
  </p>
  <h3>What’s a Blockchain?</h3><br>
  <p>
  Simply put, a blockchain is a database.<br>
  However, there is one huge difference between how you probably currently think of a database and how a blockchain database works.<br>
  In most cases, a traditional database sits on one computer or in one location.<br>
  Even if a company has redundant servers around the world, the data might only be backed up between 3 to 5 locations. On top of that, these companies collectively spend billions of dollars a year on cyber security, to protect this data.<br>
  With a blockchain database, the data can be backed up on potentially thousands of computers all over the world, for a much, much lower cost. The information in these databases is heavily encrypted and sometimes files are broken up into pieces, so even if one piece is exposed, it will not expose the entire file.<br>
  If the information on one server does become compromised by hackers, the other copies of the databases have to “agree” that the compromised data was a legitimate change to the data. If the other copies do not agree, then the change is rejected and it is changed back to match the others.<br>
  Obviously, this is an oversimplified explanation of the technology, but I hope that you are starting to see the benefits.<br>
  </p>


  <h3>Basic definitions</h3><br>
  <p>
  <b>Address</b> – A hexadecimal string which represents the unique ID of a wallet on the block chain. It is analogous to an account number.<br>
  <b>ATH</b> – All time high, the highest the price has ever been.<br>
  <b>Blockchain</b> - a digital ledger in which transactions made in bitcoin or another cryptocurrency are recorded chronologically and publicly. In Ethereum’s case, it also records the execution state of applications on the platform.<br>
  <b>Cold Storage</b> – Storing crypto away from the internet. Meaning on a paper wallet, in a hardware wallet, or on an air-gapped machine.<br>
  <b>Confirmation</b> – A transaction is confirmed when it has been verified by miners on the blockchain.<br>
  <b>Crowdsale/ICO/Token Sale</b> - ICO stands for Initial Coin Offering. They are the cryptocurrency equivalent of an IPO. Either all or some of a currency is sold at a certain time to raise money for development. Other rules and things apply and it varies from ICO to ICO. Be wary of ICOs, many ICOs have little to no work done yet and don’t deserve your money - please do a proper assessment before buying in to an ICO.<br>
  <b>Exchange</b> – Where you buy and sell your traded commodity. In our case: GDAX, Poloniex, Kraken, etc.<br>
  <b>Fiat</b> - Fiat money is currency that a government has declared to be legal tender, but it is not backed by a physical commodity.<br>
  <b>The Flippening</b> - The point at which Ethereum’s market cap, and overall block chain dominance, overtakes Bitcoin’s.<br>
  <b>FOMO</b> - Fear of Missing Out. This is a term used to describe the act of purchasing a commodity while it is on a bull run. It often carries a negative connotation, in that FOMO may cause the price to be artificially high and indicate that a correction is coming.<br>
  <b>FUD</b> - Fear, Uncertainty and Doubt. This term is used to describe the malicious spread of negativity. This is often done with the goal of causing inexperienced members to sell, or possibly cause a temporary dip in price.<br>
  <b>Hardware Wallet</b> – a cryptographically secure piece of hardware designed to keep wallet information secure. E.g. Trezor wallet, ledger nano, etc.<br>
  <b>Hodl</b> – The act of buying and holding. A play on the word hold.<br>
  <b>ICO</b> – Initial Coin/Token Offering. When a new coin is being sold at a base price before the launch of the service it is associated with.<br>
  <b>Mining</b> - The act of contributing processing power to a blockchain network to help determine the next block. Incentivized by the chain providing a reward for doing so.<br>
  <b>Paper Wallet</b> – A printed document containing the information linked to your wallet. I.e. your private key, public key, etc.<br>
  <b>Private Key</b> - This is like the key to your home. It can unlock your wallet and everything inside it for whatever transaction you (or whoever has it) choose. It is not advisable to share this with ANYONE.<br>
  <b>Public Key</b> - Your wallet address. This is the key you will share with people in order to have Ether sent to you or requested from you.<br>
  <b>Smart Contract</b> - A piece of code that can be broadcasted to the blockchain and executed. Used to write applications that run distributed across the platform.<br>
  <b>Staking</b> - In a Proof of Stake system, this generally means leaving your coins in your wallet to increase their stake in an attempt to net rewards from block creation.<br>
  </p>

  <h3>Trading definitions</h3><br>
  <p>
  <b>Arbitrage</b> – The act of purchasing coins on one exchange and selling them on another. This is usually done to exploit a price difference between exchanges.<br>
  <b>Bear Market</b> – A prolonged downward trend of a traded commodity. This is the opposite of a bull market.<br>
  <b>Bull Market</b> – A prolonged upward trend of a traded commodity. This is the opposite of a bear market.<br>
  <b>Correction</b> - A correction is a reverse movement, usually negative, of at least 10% in a stock, bond, commodity or index to adjust for an overvaluation. Corrections are generally temporary price declines interrupting an uptrend in the market or an asset.<br>
  <b>Liquidation</b> - Most exchanges employ a policy of forced liquidation in the event your collateral does not cover your current loss on margin trades. What this means is that your loan/margin will be closed and the collateral is used to pay the loss.<br>
  <b>Long Position (A long)</b> - Making a purchase with the hope that the item will increase in value so it can be sold for a profit. This is what most investors do.<br>
  <b>Margin Short (Shorting)</b> - This is the act of selling something that you’ve borrowed with the hope of being able to buy it back later at a lower price.<br>
  <b>Margin Trading</b> – Trading on money that has been loaned to you by an exchange. A deposit of capital must be placed to receive said loan.<br>
  <b>Market Order</b> - Placing either a buy or a sell order on the market with no regard for price. The market will buy or sell $x.xx/yy eth, for the best price currently available.<br>
  <b>Technical Analysis</b> (TA) - financial analysis that uses patterns in market data to identify trends and make predictions.<br>
  <b>Stop Buy</b> - An order which is triggered by the act of a traded commodity going above a price set by the trading party.<br>
  <b>Trade Volume</b> - This is the amount of trade done on a currency.It is an important metric as it can show you the amount of interest there is versus other coins. It also shows that trade is actually being done with the currency.<br>
  <b>Stop Loss</b> - An order which is triggered by the act of a traded commodity falling below a price set by the trading party.<br>
  <b>Volatility</b> - This refers to how often the price of a currency is changing. The opposite of volatile is stable.<br>
  <b>Whales</b> - Traders with massive amounts of the currency being traded. They are able to sell and buy in quantities large enough to manipulate the market price in the short term.<br>
  </p>



